import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

// Copy backend server folder to functions
const source = path.join(__dirname, '../Library_Inventory_Backend/server');
const destination = path.join(__dirname, 'server');

function copyRecursiveSync(src, dest) {
  const exists = fs.existsSync(src);
  const stats = exists && fs.statSync(src);
  const isDirectory = exists && stats.isDirectory();
  
  if (isDirectory) {
    if (!fs.existsSync(dest)) {
      fs.mkdirSync(dest, { recursive: true });
    }
    fs.readdirSync(src).forEach((childItemName) => {
      copyRecursiveSync(
        path.join(src, childItemName),
        path.join(dest, childItemName)
      );
    });
  } else {
    fs.copyFileSync(src, dest);
  }
}

// Clean destination folder
if (fs.existsSync(destination)) {
  fs.rmSync(destination, { recursive: true, force: true });
}

// Copy files
copyRecursiveSync(source, destination);

console.log('Backend files copied to functions folder');
